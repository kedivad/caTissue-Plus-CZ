package edu.wustl.catissuecore.dto;

public class SCGEventPointDTO {
	
	 String scgId;
	 String eventPointId;
	 String eventPointLabel;
	 String scgName;
	 Double studyCalendarEventPoint;
	
	/**
	 * @return
	 */
	public String getScgId() {
		return scgId;
	}
	
	/**
	 * @param scgId
	 */
	public void setScgId(String scgId) {
		this.scgId = scgId;
	}
	/**
	 * @return
	 */
	public String getEventPointId() {
		return eventPointId;
	}
	/**
	 * @param eventPointId
	 */
	public void setEventPointId(String eventPointId) {
		this.eventPointId = eventPointId;
	}
	/**
	 * @return
	 */
	public String getEventPointLabel() {
		return eventPointLabel;
	}
	/**
	 * @param eventPointLabel
	 */
	public void setEventPointLabel(String eventPointLabel) {
		this.eventPointLabel = eventPointLabel;
	}
	/**
	 * @return
	 */
	public String getScgName() {
		return scgName;
	}
	/**
	 * @param scgName
	 */
	public void setScgName(String scgName) {
		this.scgName = scgName;
	}
	/**
	 * @return
	 */
	public Double getStudyCalendarEventPoint() {
		return studyCalendarEventPoint;
	}
	/**
	 * @param studyCalendarEventPoint
	 */
	public void setStudyCalendarEventPoint(Double studyCalendarEventPoint) {
		this.studyCalendarEventPoint = studyCalendarEventPoint;
	}
	
	

}
